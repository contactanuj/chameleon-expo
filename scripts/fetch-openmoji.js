/*
 * fetch-openmoji.js - regenerates assets/ch-art.js (inline Picture-edition art).
 *
 * Collects every emoji used in the picture topics, downloads its OpenMoji color
 * SVG, minifies it, and writes assets/ch-art.js (emoji -> inline SVG string).
 * Cells without art fall back to the system emoji glyph.
 *
 * OpenMoji (https://openmoji.org) - emoji artwork licensed CC BY-SA 4.0.
 * Run: node scripts/fetch-openmoji.js   (needs network + Node 18+ for global fetch)
 */
'use strict';
var fs = require('fs');
var path = require('path');
var ROOT = path.join(__dirname, '..');
var CONTENT = require(path.join(ROOT, 'assets', 'ch-content.js'));

var set = {};
CONTENT.TOPICS.forEach(function (t) { if (t.type === 'picture') t.items.forEach(function (e) { set[e] = true; }); });
var emojis = Object.keys(set);
console.log('unique picture emoji:', emojis.length);

function omName(emoji, keepFE0F) {
  var cps = [];
  for (var ch of emoji) { var c = ch.codePointAt(0); if (c === 0xFE0F && !keepFE0F) continue; cps.push(c.toString(16).toUpperCase()); }
  return cps.join('-');
}
function minify(svg) {
  return svg.replace(/<\?xml[\s\S]*?\?>/g, '').replace(/<!--[\s\S]*?-->/g, '')
    .replace(/\s+(width|height)="[^"]*"/g, '').replace(/>\s+</g, '><').replace(/\s{2,}/g, ' ').trim();
}
function url(name) { return 'https://raw.githubusercontent.com/hfg-gmuend/openmoji/master/color/svg/' + name + '.svg'; }
async function fetchOne(emoji) {
  for (var a = 0; a < 2; a++) {
    try { var r = await fetch(url(omName(emoji, a === 1))); if (r.status === 200) { var t = await r.text(); if (/<svg/i.test(t)) return minify(t); } } catch (e) {}
  }
  return null;
}

(async function () {
  var art = {}, missing = [], i = 0;
  async function worker() { while (i < emojis.length) { var e = emojis[i++]; var s = await fetchOne(e); if (s) art[e] = s; else missing.push(e); } }
  await Promise.all([worker(), worker(), worker(), worker(), worker(), worker()]);
  var body = Object.keys(art).map(function (e) { return '  ' + JSON.stringify(e) + ': ' + JSON.stringify(art[e]); }).join(',\n');
  var out =
    '/*\n * ch-art.js - GENERATED by scripts/fetch-openmoji.js. Do not edit by hand.\n' +
    ' * Picture-edition artwork by OpenMoji (https://openmoji.org) - CC BY-SA 4.0.\n */\n' +
    '(function (root, factory) {\n  var A = factory();\n' +
    '  if (typeof module !== "undefined" && module.exports) module.exports = A;\n' +
    '  if (root) root.CH_ART = A;\n' +
    '})(typeof window !== "undefined" ? window : (typeof globalThis !== "undefined" ? globalThis : this), function () {\n' +
    '  "use strict";\n  return {\n' + body + '\n  };\n});\n';
  fs.writeFileSync(path.join(ROOT, 'assets', 'ch-art.js'), out, 'utf8');
  console.log('wrote assets/ch-art.js:', Object.keys(art).length, 'illustrations,', Math.round(Buffer.byteLength(out, 'utf8') / 1024) + 'KB');
  if (missing.length) console.log('MISSING (emoji fallback):', missing.join(' '));
})();
